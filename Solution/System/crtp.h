template <typename T>
class Singleton {
public:
    static T& instance() {
        static T _instance;
        return _instance;
    }

protected:
    Singleton() = default;
    ~Singleton() = default;

private:
    Singleton(const Singleton&) = delete;
    Singleton& operator=(const Singleton&) = delete;
};



